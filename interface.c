/************************************************
 * 関数名：Inter_Sw
 * 概要：スイッチを使う
 * 引数：なし
 * 返り値：スイッチ ON -> 1 , OFF -> 0
 ***************************************************/
unsigned char Inter_Sw(void){
    unsigned char sw=PORTAbits.RA5 ^ 0b1;
    return sw;
}
